SELECT 
  V.ID_VENDA, 
  V.ID_BOMBA, 
  V.QTD, 
  V.PRECOVENDA, 
  V.PERCENTUALIMPOSTO, 
  V.TOTALVENDA, 
  V.DATAVENDA, 
  V.HORAVENDA, 
  P.DESCRICAO 
FROM VENDAS V
INNER JOIN CAD_BOMBAS B ON B.ID_BOMBA = V.ID_BOMBA
INNER JOIN CAD_PRODUTO P ON P.ID_PRODUTO = B.ID_PRODUTO
WHERE V.DATAVENDA = CURRENT_DATE
ORDER BY V.ID_VENDA DESC